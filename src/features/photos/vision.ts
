import { tgGetFileUrl } from "../../utils/telegram";

export async function processPhotoWithGemini(env: any, chatId: number, prompt: string) {
  if (!env.SENTI_CACHE) return { text: "⚠️ Нема кешу для фото." };
  if (!env.CF_VISION) return { text: "⚠️ Не задано CF_VISION ключ." };

  const fileId = await env.SENTI_CACHE.get(`lastPhoto:${chatId}`);
  if (!fileId) return { text: "⚠️ Спочатку надішли фото." };

  // отримати URL фото з Telegram
  const fileInfo = await fetch(`https://api.telegram.org/bot${env.BOT_TOKEN}/getFile?file_id=${fileId}`).then(r => r.json());
  const path = fileInfo?.result?.file_path;
  if (!path) return { text: "⚠️ Не вдалося отримати фото." };

  const photoUrl = `https://api.telegram.org/file/bot${env.BOT_TOKEN}/${path}`;
  const img = await fetch(photoUrl).then(r => r.arrayBuffer());
  const base64 = btoa(String.fromCharCode(...new Uint8Array(img)));

  // виклик Gemini Vision
  const body = {
    contents: [
      {
        parts: [
          { text: prompt },
          { inline_data: { mime_type: "image/jpeg", data: base64 } }
        ]
      }
    ]
  };

  const res = await fetch(
    `https://generativelanguage.googleapis.com/v1beta/models/gemini-pro-vision:generateContent?key=${env.CF_VISION}`,
    {
      method: "POST",
      headers: { "content-type": "application/json" },
      body: JSON.stringify(body)
    }
  ).then(r => r.json());

  const text = res?.candidates?.[0]?.content?.parts?.[0]?.text || "⚠️ Gemini Vision не відповів.";
  return { text };
}