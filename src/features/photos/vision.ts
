import { Env } from "../../index";
import { tgSendMessage } from "../../utils/telegram";

export async function processPhotoWithGemini(env: Env, chatId: number, prompt: string): Promise<{ text: string }> {
  if (!env.SENTI_CACHE) return { text: "⚠️ Немає кешу для фото." };

  const fileId = await env.SENTI_CACHE.get(`lastPhoto:${chatId}`);
  if (!fileId) return { text: "⚠️ Фото не знайдено. Спочатку надішли фото." };

  // ⚡️ Будуємо URL до фото
  const url = `https://api.telegram.org/file/bot${env.BOT_TOKEN}/${fileId}`;

  // 🔑 Використовуємо Cloudflare AI (Gemini Vision)
  const model = "@cf/google/gemini-1.5-flash-001";
  const input = {
    messages: [
      { role: "system", content: "Ти — помічник, що аналізує фото і відповідає коротко." },
      { role: "user", content: [
        { type: "input_text", text: prompt },
        { type: "input_image", image_url: url }
      ]}
    ]
  };

  const resp = await fetch(`https://api.cloudflare.com/client/v4/accounts/${env.CF_ACCOUNT_ID}/ai/run/${model}`, {
    method: "POST",
    headers: {
      Authorization: `Bearer ${env.CLOUDFLARE_API_TOKEN}`,
      "Content-Type": "application/json"
    },
    body: JSON.stringify(input),
  });

  if (!resp.ok) {
    const errText = await resp.text();
    return { text: `⚠️ AI error: ${errText}` };
  }

  const data = await resp.json();
  return { text: data?.result?.response || "🤔 Не вдалося отримати відповідь." };
}