// Обробка фото + Vision через Cloudflare AI (Gemini 1.5 Flash) — REST API
import { tgGetFilePath, tgSendMessage } from "../../utils/telegram";

type EnvAll = {
  BOT_TOKEN: string;

  // KV
  SENTI_CACHE: KVNamespace;

  // Secrets для Workers AI через REST
  CLOUDFLARE_API_TOKEN?: string;
  CLOUDFLARE_ACCOUNT_ID?: string;

  // Альтернативні назви зі скрінів (сумісність)
  CF_ACCOUNT_ID?: string;
  CF_VISION?: string;
};

export const cacheKey = (chatId: number) => `last_photo:${chatId}`;

export async function handleIncomingPhoto(env: EnvAll, chatId: number, fileId: string) {
  await env.SENTI_CACHE.put(cacheKey(chatId), fileId, { expirationTtl: 600 });
  await tgSendMessage(env, chatId, "Фото отримав ✅ Тепер надішли коротку підказку текстом — що саме проаналізувати?");
}

export async function runVision(env: EnvAll, chatId: number, prompt: string) {
  const fileId = await env.SENTI_CACHE.get(cacheKey(chatId));
  if (!fileId) {
    await tgSendMessage(env, chatId, "Спершу надішли фото, потім — текст-підказку. 😉");
    return;
  }

  const filePath = await tgGetFilePath(env, fileId);
  if (!filePath) {
    await tgSendMessage(env, chatId, "Не вдалось отримати шлях до фото з Telegram. Спробуй надіслати картинку ще раз.");
    return;
  }
  const fileUrl = `https://api.telegram.org/file/bot${env.BOT_TOKEN}/${filePath}`;

  const text = await runWorkersAI(env, fileUrl, prompt);
  if (!text) {
    const hints: string[] = [];
    if (!env.CLOUDFLARE_API_TOKEN && !env.CF_VISION) hints.push("додай CLOUDFLARE_API_TOKEN (або CF_VISION).");
    if (!env.CLOUDFLARE_ACCOUNT_ID && !env.CF_ACCOUNT_ID) hints.push("додай CLOUDFLARE_ACCOUNT_ID (або CF_ACCOUNT_ID).");
    await tgSendMessage(env, chatId, "Не вдалось отримати відповідь від AI. Перевір секрети Cloudflare." + (hints.length ? "\n- " + hints.join("\n- ") : ""));
    return;
  }

  await tgSendMessage(env, chatId, text);
}

async function runWorkersAI(env: EnvAll, imageUrl: string, prompt: string): Promise<string | null> {
  const accountId = env.CLOUDFLARE_ACCOUNT_ID || env.CF_ACCOUNT_ID;
  const apiToken  = env.CLOUDFLARE_API_TOKEN || env.CF_VISION;
  if (!accountId || !apiToken) return null;

  const url = `https://api.cloudflare.com/client/v4/accounts/${accountId}/ai/run/@cf/google/gemini-1.5-flash-001`;
  const res = await fetch(url, {
    method: "POST",
    headers: { Authorization: `Bearer ${apiToken}`, "content-type": "application/json" },
    body: JSON.stringify({ input_text: prompt, image: imageUrl }),
  });
  const data = await res.json<any>().catch(() => ({}));
  if (data?.success === false) return null;

  const result = data?.result ?? data;
  const text =
    result?.response ||
    result?.text ||
    result?.output_text ||
    (Array.isArray(result) && result[0]?.response) ||
    null;

  return text ? String(text) : JSON.stringify(result ?? data);
}